package net.infiltration;

public class Swap {
    public String key1;
    public String key2;

    public Swap(String key1, String key2){
        this.key1 = key1;
        this.key2 = key2;
    }

    public String getKey1(){
        return key1;
    }
    public String getKey2(){
        return key2;
    }
}
